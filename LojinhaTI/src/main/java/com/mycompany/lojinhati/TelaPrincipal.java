    private void btnAdicionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAdicionarActionPerformed
        String tipoComputador = "";
        String sistema = "";
        String tipoHD = "";
        
        if (rboPC.isSelected()){
            tipoComputador = rboPC.getText();
        }
        else if(rboNote.isSelected()){
            tipoComputador = rboNote.getText();
        }
        else if(rboServer.isSelected()){
            tipoComputador = rboServer.getText();
        }
        else{
            JOptionPane.showMessageDialog(rootPane, "Selecione um tipo de computador!");
            return;
        }
        
        sistema = grupoSO.getSelection().getActionCommand(); 
        
        if(cboHD.getSelectedIndex() > 0){
            tipoHD = cboHD.getSelectedItem().toString();
        }else{
            JOptionPane.showMessageDialog(rootPane, "Selecione um HD!");
            return;
        }
        
        //Resgatar dados dos Checkbox
        String acessorios = "";
        
        if(chkMochila.isSelected()){
            acessorios += chkMochila.getText() + " ";
        }
        if(chkHeadset.isSelected()){
            acessorios += chkHeadset.getText() + " ";
        }
        if(chkMouse.isSelected()){
            acessorios += chkMouse.getText() + " ";
        }
        
        String garantiasSelecionadas = "";
        List <String> garantias = lstGarantias.getSelectedValuesList();
        
        //Para cada item na lista de garantias, fa√ßa uma coisa.
        for(String item: garantias){
            garantiasSelecionadas += item + ", ";
        }
        
        //Adicionar os itens selecionados √† tabela produto
        //Acesso a camada model para adicionar/remover linhas ou colunas
        DefaultTableModel modelo = (DefaultTableModel)tblProdutos.getModel();
        
        //Adiciono as op√ß√µes √† camada de modelagem da tabela
        modelo.addRow(new String[]{tipoComputador, sistema, tipoHD, acessorios, garantiasSelecionadas});
        
        //JOptionPane.showMessageDialog(rootPane, "Tipo computador: "+tipoComputador +"\nSistema: "+sistema +"\nTipo HD: "+tipoHD +"\nAcess√≥rios: "+acessorios);
    }//GEN-LAST:event_btnAdicionarActionPerformed

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        //Verificar se tem linhas selecionadas
        int linhaSelecionada = tblProdutos.getSelectedRow();
        DefaultTableModel modelo = (DefaultTableModel)tblProdutos.getModel();
        
        // -1 √© quando n√£o tem linha selecionada.
        if(linhaSelecionada == -1){
            JOptionPane.showMessageDialog(rootPane, "Selecione uma linha antes de excluir!");
        }
        else{
            modelo.removeRow(linhaSelecionada);
        }
        
        
    }//GEN-LAST:event_btnExcluirActionPerformed

